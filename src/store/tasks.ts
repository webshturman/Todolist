export const summSalary = (sum:number, add:number):number => {
    return sum + add
}
export const multSalary = (sum:number, add:number):number => {
    return sum * add
}
export const divSalary = (sum:number, add:number):number => {
    return sum / add
}
export const deductSalary = (sum:number, add:number):number => {
    return sum - add
}